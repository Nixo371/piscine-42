void	ft_rev_int_tab(int *tab, int size)
{
	int	buf;
	int	i;

	i = 0;
	while (i < size / 2)
	{
		buf = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - i - 1] = buf;
		i++;
	}
}
